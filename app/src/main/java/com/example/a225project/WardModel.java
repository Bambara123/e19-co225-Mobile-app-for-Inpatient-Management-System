package com.example.a225project;

public class WardModel {

    private String id, name;

    public WardModel(){

    }

    public WardModel(String id, String name){
        this.id = id;
        this.name = name;

    }


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }






}
